:"NAVEGAÇÃO NO TERMINAL
    Navegação pelo terminal é feita pelo comando cd que significa Change Diretory"

ls
:"Claro que pra saber as pastas do diretorio atual é só usar o comando ls. Esse
    comando mostra todas as pastas do diretório atual. Mas eu vou fazer uma esplicação
    sobre isso tbm em outro arquivo."

ls --help
:"Abre a documentação do comando ls."

ls -l
:"Quando vc adiciona o comando -l para ls, os diretórios aparecem em lista."

ls -la
:"Ja ls -la é usado para mostrar todos os diretório também em listas, mas dessa vez
    mostra também os diretórios ocultos que começam com ponto."

pwd
:"O comando pwd significa Print Work Directory e mostra o caminho completo do diretório
em que vc se encontra. Por exemplo: /home/meuDiretorio"

cd Downloads
:" Esse comando significa Change Diretory. Quando vc escreve o comando cd e o nome de 
    um diretório, você entra na pasta selecionada, mas só funciona para as pastas 
    do diretório atual. No caso de um diretório fora do atual é nescessário
    usar ./Downloads"

cd --help
:"Abre a documentação de comandos de ls."

cd ./Downloads
:"Para um diretório fora da pasta atual se adiciona ./ antes do nome do diretorio. 
    Importantes escrever os nomes corretamente e considerando as letras maiusculas 
    e minusculas."

cd ..
:"Ja cd .. é usar pra sair de uma pasta. Usando esse comando vc volta para a pasta
    anterior."

cd ~
:"Cada usuario tem um diretório por padrão do qual o nome é o nome do usuario. Para
    acessar esse diretório de forma rápida, você pode usar o cd com ~."

mkdir nome-da-pasta
:"mkdir significa making diretory, e é o comando usado para criar diretórios, seguindo do nome do diretório."

mkdir --help
:'Abre a documentação de mkdir'
